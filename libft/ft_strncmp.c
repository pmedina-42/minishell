#include <string.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	*str1;
	unsigned char	*str2;

	if (n == 0)
		return (0);
	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	while (str1[i] == str2[i] && str1[i] != '\0' && i < n - 1)
		i++;
	if (str1[i] == str2[i])
		return (0);
	if (str1[i] > str2[i])
		return (1);
	return (-1);
}
